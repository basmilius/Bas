declare(strict_types=1);

namespace Columba\Contract\Rule;

use function filter_var;

/**
 * Class RuleEmail
 *
 * @author Bas Milius <bas@mili.us>
 * @package Columba\Contract\Rule
 * @since 1.6.0
 */
final class RuleEmail extends AbstractRule
{

	/**
	 * {@inheritdoc}
	 * @author Bas Milius <bas@mili.us>
	 * @since 1.6.0
	 */
	public final function met(&$value): bool
	{
		if (filter_var($value, FILTER_VALIDATE_EMAIL))
			return true;

		return $this->breach('The given value is not an e-mailaddress.', $value);
	}

}
